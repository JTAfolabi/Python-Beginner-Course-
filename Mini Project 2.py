# Draw drawings with Turtle package
# Import Turtle
import turtle
# Set-up in Turtle
turtle.bgcolor("black")
turtle.pensize(2)
turtle.color("red")
# Draw a square
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.forward(50)
turtle.left(90)
turtle.done()
